    export default {
        name: 'fxpjdf',
        data() {
            return {
                query: {
                    address: '',
                    name: '',
                    pageIndex: 1,
                    pageSize: 10
                },
                tableData: [],
                tableDataR: [],
                multipleSelection: [],
                delList: [],
                editVisible: false,
                addVisible:false,
                pageTotal: 0,
                form: {},
                searchInfo:'',
                addForm:{},
                idx: -1,
                id: -1
            };
        },
        created() {
            this.getData();
        },
        methods: {
            // 获取 easy-mock 的模拟数据
            getData() {
                fetchData(process.env.BASE_URL + '/高后果区识别计划.json',this.query).then(res => {
                    console.log(res);
                    this.tableData = res.list;
                    this.tableDataR = res.list;
                    this.pageTotal = res.pageTotal || 50;
                });
            },
            handleAdd(){
                this.addVisible=true
            },
            // 触发搜索按钮
            handleSearch() {
                let c= this
                this.tableDataR=this.tableData.filter((item)=>{
                    for(let i in item){
                        if(item[i].toString().indexOf(c.searchInfo)!=-1){
                            return true
                        }
                    }
                    return false
                })
            },
            // 删除操作
            handleDelete(index, row) {
                // 二次确认删除
                this.$confirm('确定要删除吗？', '提示', {
                    type: 'warning'
                }).then(() => {
                    this.$message.success('删除成功');
                    this.tableData.splice(index, 1);
                })
                    .catch(() => {});
            },
            // 多选操作
            handleSelectionChange(val) {
                this.multipleSelection = val;
            },
            handleRowClick(row,column,event){
                this.$refs.multipleTable.toggleRowSelection(row);
            },
            delAllSelection() {
                this.$confirm('确定要删除吗？', '提示', {
                    type: 'warning'
                }).then(() => {
                    this.tableData=this.tableData.filter(v =>{
                        return this.multipleSelection.every(x=>{
                            return v['id']!=x['id']
                        })
                    })
                    this.$message.success(`删除成功`);
                    this.multipleSelection = [];
                }).catch(() => {});

                // const length = this.multipleSelection.length;
